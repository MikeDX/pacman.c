    return (sapp_desc) {
        .init_cb = init,
        .frame_cb = frame,
        .cleanup_cb = cleanup,
        .event_cb = input,
        .width = DISPLAY_TILES_X * TILE_WIDTH * 2,
        .height = DISPLAY_TILES_Y * TILE_HEIGHT * 2,
        .window_title = "pacman.c"
    };
