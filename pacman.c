static void init(void) {
    // setup sokol libs
    sg_setup(&(sg_desc){
        .context = sapp_sgcontext()
    });
    stm_setup();
    saudio_setup(&(saudio_desc){ 0 });

    // initialize subsystems
    gfx_init();

    // start into intro screen
    start(&state.intro.started);
}

static void frame(void) {
    // FIXME: decouple game_tick() from refresh rate
    pacman_tick();
    gfx_draw();
}

static void input(const sapp_event* ev) {
    if (state.input.enabled) {
        if ((ev->type == SAPP_EVENTTYPE_KEY_DOWN) || (ev->type == SAPP_EVENTTYPE_KEY_UP)) {
            bool btn_down = ev->type == SAPP_EVENTTYPE_KEY_DOWN;
            switch (ev->key_code) {
                case SAPP_KEYCODE_UP:
                case SAPP_KEYCODE_W:
                    state.input.up = state.input.any = btn_down;
                    break;
                case SAPP_KEYCODE_DOWN:
                case SAPP_KEYCODE_S:
                    state.input.down = state.input.any = btn_down;
                    break;
                case SAPP_KEYCODE_LEFT:
                case SAPP_KEYCODE_A:
                    state.input.left = state.input.any = btn_down;
                    break;
                case SAPP_KEYCODE_RIGHT:
                case SAPP_KEYCODE_D:
                    state.input.right = state.input.any = btn_down;
                    break;
                default:
                    state.input.any = btn_down;
                    break;
            }
        }
    }
}

static void cleanup(void) {
    sg_shutdown();
    saudio_shutdown();
}

/*== HELPER FUNCTIONS ========================================================*/

// activate trigger in next tick
static void start(trigger_t* t) {
    t->tick = state.tick + 1;
}

// activate trigger in any future tick
static void start_after(trigger_t* t, uint32_t ticks) {
    t->tick = state.tick + ticks;
}

// deactivate a trigger
static void disable(trigger_t* t) {
    t->tick = 0xFFFFFFFF;
}

// check if a trigger is triggered
static bool now(trigger_t t) {
    return t.tick == state.tick;
}

// return the number of ticks since trigger was triggered
static uint32_t since(trigger_t t) {
    if (state.tick >= t.tick) {
        return state.tick - t.tick;
    }
    else {
        return 0xFFFFFFFF;
    }
}

// check if trigger is between begin and end tick
static bool phase(trigger_t t, uint32_t begin, uint32_t end) {
    assert((begin >= 0) && (begin < end));
    uint32_t ticks = since(t);
    return (ticks >= begin) && (ticks < end);
}

// check if trigger was triggered N ticks ago
static bool after(trigger_t t, uint32_t ticks) {
    return since(t) == ticks;
}

// clear input state and disable input
static void input_disable(void) {
    memset(&state.input, 0, sizeof(state.input));
}

// enable input again
static void input_enable(void) {
    state.input.enabled = true;
}

// clear tile buffer
static void vid_clear(uint8_t tile_code, uint8_t color_code) {
    memset(&state.gfx.video_ram, tile_code, sizeof(state.gfx.video_ram));
    memset(&state.gfx.color_ram, color_code, sizeof(state.gfx.color_ram));
}

// put a color into the color buffer
static void vid_color(uint8_t x, uint8_t y, uint8_t color_code) {
    assert((x < DISPLAY_TILES_X) && (y < DISPLAY_TILES_Y));
    state.gfx.color_ram[y][x] = color_code;
}

// put a tile into the tile buffer (
static void vid_tile(uint8_t x, uint8_t y, uint8_t tile_code) {
    assert((x < DISPLAY_TILES_X) && (y < DISPLAY_TILES_Y));
    state.gfx.video_ram[y][x] = tile_code;
}

// put a colored tile into the tile buffer
static void vid_color_tile(uint8_t x, uint8_t y, uint8_t color_code, uint8_t tile_code) {
    assert((x < DISPLAY_TILES_X) && (y < DISPLAY_TILES_Y));
    state.gfx.video_ram[y][x] = tile_code;
    state.gfx.color_ram[y][x] = color_code;
}

// translate ASCII char into "NAMCO char"
static char conv_char(char c) {
    switch (c) {
        case ' ':   c = 64; break;
        case '/':   c = 58; break;
        case '-':   c = 59; break;
        case '\"':  c = 38; break;
        case '!':   c = 'Z'+1; break;
        default: break;
    }
    return c;
}

// put colored text into the tile buffer
static void vid_color_text(uint8_t x, uint8_t y, uint8_t color_code, const char* text) {
    assert((x < DISPLAY_TILES_X) && (y < DISPLAY_TILES_Y));
    uint8_t chr;
    while ((chr = (uint8_t) *text++)) {
        if (x < DISPLAY_TILES_X) {
            state.gfx.video_ram[y][x] = conv_char(chr);
            state.gfx.color_ram[y][x] = color_code;
            x++;
        }
        else {
            break;
        }
    }
}

// put text without color into the tile buffer
static void vid_text(uint8_t x, uint8_t y, const char* text) {
    assert((x < DISPLAY_TILES_X) && (y < DISPLAY_TILES_Y));
    uint8_t chr;
    while ((chr = (uint8_t) *text++)) {
        if (x < DISPLAY_TILES_X) {
            state.gfx.video_ram[y][x] = conv_char(chr);
            x++;
        }
        else {
            break;
        }
    }
}

// clear/reset all sprites by placing them outside the screen
static void spr_clear(void) {
    memset(&state.gfx.sprite, 0, sizeof(state.gfx.sprite));
}

// initialize a sprite
static void spr_init(int index, sprite_t sprite) {
    assert((index >= 0) && (index < NUM_SPRITES));
    state.gfx.sprite[index] = sprite;
    state.gfx.sprite[index].enabled = true;
}

// set sprite to animated pacman
static void spr_anim_pacman(int index, dir_t dir) {
    assert((index >= 0) && (index < NUM_SPRITES));
    assert((dir >= 0) && (dir < NUM_DIRS));
    // animation frames for horizontal and vertical movement
    static const uint8_t tiles[2][4] = {
        { 44, 46, 48, 46 }, // horizontal (needs flipx)
        { 45, 47, 48, 47 }  // vertical (needs flipy)
    };

    sprite_t* spr = &state.gfx.sprite[index];
    uint32_t phase = (state.tick / 4) & 3;
    spr->tile  = tiles[dir & 1][phase];
    spr->flipx = (dir == DIR_LEFT);
    spr->flipy = (dir == DIR_UP);
}

// set sprite to animated ghost
static void spr_anim_ghost(int index, dir_t dir) {
    assert((index >= 0) && (index < NUM_SPRITES));
    assert((dir >= 0) && (dir < NUM_DIRS));
    static const uint8_t tiles[4][2]  = {
        { 32, 33 }, // right
        { 34, 35 }, // down
        { 36, 37 }, // left
        { 38, 39 }, // up
    };
    uint32_t phase = (state.tick / 4) & 1;
    sprite_t* spr = &state.gfx.sprite[index];
    spr->tile = tiles[dir][phase];
    spr->flipx = false;
    spr->flipy = false;
}

// set sprite to animated, frightened ghost
static void spr_anim_ghost_frightened(int index) {
    assert((index >= 0) && (index < NUM_SPRITES));
    static const uint8_t tiles[4] = { 28, 29 };
    uint32_t phase = (state.tick / 4) & 1;
    sprite_t* spr = &state.gfx.sprite[index];
    spr->tile = tiles[phase];
    spr->flipx = false;
    spr->flipy = false;
}

/*== TOP-LEVEL GAME CODE =====================================================*/
static void pacman_tick(void) {
    state.tick++;

    // check for game state change
    if (now(state.intro.started)) {
        state.gamestate = GAMESTATE_INTRO;
    }
    if (now(state.game.started)) {
        state.gamestate = GAMESTATE_GAME;
    }
    if (now(state.hiscore.started)) {
        state.gamestate = GAMESTATE_HISCORE;
    }

    // call the top-level game state update function
    switch (state.gamestate) {
        case GAMESTATE_INTRO:
            intro_tick();
            break;
        case GAMESTATE_GAME:
            game_tick();
            break;
        case GAMESTATE_HISCORE:
            hiscore_tick();
            break;
    }

    // handle fadein/fadeout
    gfx_fade();
}

/*== INTRO GAMESTATE CODE ====================================================*/

static void intro_tick(void) {

    // on intro-state enter, enable input and draw any initial text
    if (now(state.intro.started)) {
        start(&state.gfx.fadein);
        input_enable();
        vid_clear(0x40, 0xF);
        vid_text(3, 0,  "1UP   HIGH SCORE   2UP");
        vid_text(5, 1,  "00");
        vid_text(7, 5,  "CHARACTER / NICKNAME");
        vid_text(3, 35, "CREDIT  0");
        spr_clear();
        disable(&state.intro.chase);
    }

    // draw the animated 'ghost image.. name.. nickname' lines
    uint32_t delay = 30;
    const char* names[] = { "-SHADOW", "-SPEEDY", "-BASHFUL", "-POKEY" };
    const char* nicknames[] = { "BLINKY", "PINKY", "INKY", "CLYDE" };
    for (int i = 0; i < 4; i++) {
        const uint8_t color = 2*i + 1;
        const uint8_t y = 3*i + 6;
        // 2*3 ghost image created from tiles (no sprite!)
        delay += 30;
        if (after(state.intro.started, delay)) {
            vid_color_tile(4, y+0, color, TILE_GHOST+0); vid_color_tile(5, y+0, color, TILE_GHOST+1);
            vid_color_tile(4, y+1, color, TILE_GHOST+2); vid_color_tile(5, y+1, color, TILE_GHOST+3);
            vid_color_tile(4, y+2, color, TILE_GHOST+4); vid_color_tile(5, y+2, color, TILE_GHOST+5);
        }
        // after 1 second, the name of the ghost
        delay += 60;
        if (after(state.intro.started, delay)) {
            vid_color_text(7, y+1, color, names[i]);
        }
        // after 0.5 seconds, the nickname of the ghost
        delay += 30;
        if (after(state.intro.started, delay)) {
            vid_color_text(17, y+1, color, nicknames[i]);
        }
    }

    // . 10 PTS
    // O 50 PTS
    delay += 60;
    if (after(state.intro.started, delay)) {
        vid_color_tile(10, 24, 0x10, TILE_DOT);
        vid_text(12, 24, "10 \x5D\x5E\x5F");
        vid_color_tile(10, 26, 0x10, TILE_PILL);
        vid_text(12, 26, "50 \x5D\x5E\x5F");
    }

    // blinking "press any key" text
    delay += 60;
    if (since(state.intro.started) > delay) {
        if (since(state.intro.started) & 0x20) {
            vid_color_text(3, 31, 3, "                       ");
        }
        else {
            vid_color_text(3, 31, 3, "PRESS ANY KEY TO START!");
        }
    }

    // FIXME: rewrite this with "gameplay code"
    // the animated chase sequence
    delay += 60;
    if (after(state.intro.started, delay)) {
        start(&state.intro.chase);
        vid_color_tile(4, 20, 0x10, TILE_PILL);
        int16_t x = 224;
        spr_init(0, (sprite_t) { .x=x+20, .y=156, .color = COLOR_BLINKY });
        spr_init(1, (sprite_t) { .x=x+36, .y=156, .color = COLOR_PINKY });
        spr_init(2, (sprite_t) { .x=x+52, .y=156, .color = COLOR_INKY });
        spr_init(3, (sprite_t) { .x=x+68, .y=156, .color = COLOR_CLYDE });
        spr_init(4, (sprite_t) { .x=x,    .y=156, .color = COLOR_PACMAN });
    }
    // ghosts are chasing pacman...
    if (phase(state.intro.chase, 1, 200)) {
        for (int i = 0; i < 5; i++) {
            if (i == 4) { spr_anim_pacman(i, DIR_LEFT); }
            else        { spr_anim_ghost(i, DIR_LEFT); }
            state.gfx.sprite[i].x--;
        }
    }
    // pacman is chasing and eating ghosts
    if (phase(state.intro.chase, 210, 500)) {
        for (int i = 0; i < 5; i++) {
            if (i == 4) {
                spr_anim_pacman(i, DIR_RIGHT);
                state.gfx.sprite[i].x++;
            }
            else {
                spr_anim_ghost_frightened(i);
                state.gfx.sprite[i].color = COLOR_FRIGHTENED;
                if (state.tick & 1) {
                    state.gfx.sprite[i].x++;
                }
            }
        }
    }

    // if a key is pressed, advance to game state
    if (state.input.any) {
        input_disable();
        start(&state.gfx.fadeout);
        start_after(&state.game.started, FADE_TICKS);
    }
}

/*== HISCORE GAMESTATE CODE ==================================================*/
static void hiscore_tick(void) {
    if (now(state.hiscore.started)) {
        start(&state.gfx.fadein);
        input_enable();
        vid_clear(0x40, 0xF);
        vid_text(7, 16, "HISCORE TODO!");
    }
    if (state.input.any) {
        input_disable();
        start(&state.gfx.fadeout);
        start_after(&state.intro.started, FADE_TICKS);
    }
}

